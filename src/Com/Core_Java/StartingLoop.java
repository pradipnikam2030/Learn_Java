package Com.Core_Java;

public class StartingLoop {


    public static void main(String[] args) {
        for (long i=1; i<=5 ; ++
                i) {
            System.out.println(i);
        }


        int j=1;
        for(; j<=5;) {

            System.out.println(j);
            j++;

        }

    }
}

